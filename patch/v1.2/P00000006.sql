-- Migrate existing 14-bit ACL masks to 17-bit
UPDATE db.acl SET allow = B'000' || allow, deny = B'000' || deny
WHERE bit_length(allow) < 17;

CREATE OR REPLACE FUNCTION db.ft_user_after_insert()
RETURNS trigger AS $$
BEGIN
  CASE NEW.username
  WHEN 'system' THEN
    INSERT INTO db.acl SELECT NEW.id, B'00000000000000000', B'01110000000000011';
  WHEN 'administrator' THEN
    INSERT INTO db.acl SELECT NEW.id, B'00000000000000000', B'11101111111111111';
  WHEN 'guest' THEN
    INSERT INTO db.acl SELECT NEW.id, B'11111111111111100', B'00000000000000011';
  WHEN 'apibot' THEN
    INSERT INTO db.acl SELECT NEW.id, B'00010000000000011', B'11101111111111100';
  ELSE
    INSERT INTO db.acl SELECT NEW.id, B'00000000000000000', B'00000000000000011';
  END CASE;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql
   SECURITY DEFINER
   SET search_path = kernel, pg_temp;

